package feature.Week1;

public class AreaofCircle {
    public static void main(String[] args) {
        // Declare radius
        double radius = 12;
        // Declare pi
        double PI = 3.14;
        // calculate area
        double area = radius * radius * PI;
        //print
        System.out.println("Area of the circle is: "+ area);
    }
}
